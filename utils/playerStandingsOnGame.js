class PlayerStandingsOnGame{
    constructor(name, minutes, points, rebounds, assists, steals, blocks) {
        this.name = name;
        this.minutes = minutes;
        this.points = points;
        this.rebounds = rebounds;
        this.assists = assists;
        this.steals = steals;
        this.blocks = blocks;
    }
}

module.exports = PlayerStandingsOnGame;